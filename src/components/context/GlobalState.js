export const GlobalContext = createContext(initialState);

export const GlobalProvider = ({ children }) => {
  const [state, dispatch] = useReducer(AppReducer, initialState);

  // Actions
  const resetTable = () => {
    dispatch({
      type: "RESET_TABLE",
      payload: { initialState },
    });
  };

  const filterByRecently = (rowCount) => {
    dispatch({
      type: "FILTER_BY_RECENTLY",
      payload: { initialState, rowCount },
    });
  };

  const filterByDate = (startDate, endDate) => {
    dispatch({
      type: "FILTER_BY_DATE",
      payload: { initialState, startDate, endDate },
    });
  };

  const filterByAmount = (from, to) => {
    dispatch({
      type: "FILTER_BY_AMOUNT",
      payload: { initialState, from, to },
    });
  };

  const filterByCurrency = (sign) => {
    dispatch({
      type: "FILTER_BY_CURRENCY",
      payload: { initialState, sign },
    });
  };

  const filterByExchange = (from, to) => {
    dispatch({
      type: "FILTER_BY_EXCHANGE",
      payload: { initialState, from, to },
    });
  };

  const filterByCard = (type) => {
    dispatch({
      type: "FILTER_BY_CARD",
      payload: { initialState, type },
    });
  };

  const filterByPayments = (paymentsNum) => {
    dispatch({
      type: "FILTER_BY_PAYMENTS",
      payload: { initialState, paymentsNum },
    });
  };

  const filterByCardNumber = (cardNum) => {
    dispatch({
      type: "FILTER_BY_CARD_NUMBER",
      payload: { initialState, cardNum },
    });
  };

  const filterByApproval = (approvalNum) => {
    dispatch({
      type: "FILTER_BY_APPROVAL",
      payload: { initialState, approvalNum },
    });
  };

  return (
    <GlobalContext.Provider
      value={{
        transactions: state.transactions,
        resetTable,
        filterByRecently,
        filterByDate,
        filterByAmount,
        filterByCurrency,
        filterByExchange,
        filterByCard,
        filterByPayments,
        filterByCardNumber,
        filterByApproval,
      }}
    >
      {children}
    </GlobalContext.Provider>
  );
};
